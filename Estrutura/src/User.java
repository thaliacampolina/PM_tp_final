
public class User {
	private int id;
	private int age;
	private int gender;
	private int occupation;
	private int zipCode;
	
	public User(int id, int age, int gender, int occupation, int zipCode)
	{
		this.id = id;
		this.age = age;
		this.gender = gender;
		this.occupation = occupation;
		this.zipCode = zipCode;
	}

}